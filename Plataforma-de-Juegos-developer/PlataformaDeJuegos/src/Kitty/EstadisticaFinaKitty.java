    public void guardarDatos(){
        
        String nombre1 = jLabel5.getText();
        int puntaje1 = Integer.parseInt(jLabel1.getText());
        String nombre2 = jLabel4.getText();
        int puntaje2 = Integer.parseInt(jLabel2.getText());
        
        DataKitty datosKitty = new DataKitty();
        datosKitty.comprobarArchivo();
        datosKitty.usuario(nombre1, puntaje1);
        datosKitty.usuario(nombre2, puntaje2);
    }
    
